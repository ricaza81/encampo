        //Funcion principal
        initMap = function () {
            //usamos la API para geolocalizar el usuario
            navigator.geolocation.getCurrentPosition(
                function (position) {
                    coords = {
                        lng: position.coords.longitude,
                        lat: position.coords.latitude,
                    };
                    var coords = coords;
                    setMapa(coords); //pasamos las coordenadas al metodo para crear el
                },
                function (error) {
                    console.log(error);
                }
            );
        };

        var coords = coords;
        var drawingManager;
        var selectedShape;
        var colors = ["#1E90FF", "#FF1493", "#32CD32", "#FF8C00", "#4B0082"];
        var selectedColor;
        var colorButtons = {};

        function clearSelection() {
            if (selectedShape) {
                selectedShape.setEditable(false);
                selectedShape = null;
            }
        }

        function setSelection(shape) {
            clearSelection();
            selectedShape = shape;
            shape.setEditable(true);
            selectColor(shape.get("fillColor") || shape.get("strokeColor"));
            google.maps.event.addListener(shape.getPath(), "set_at", calcar);
            google.maps.event.addListener(shape.getPath(), "insert_at", calcar);
        }

        function calcar() {
            var area = google.maps.geometry.spherical.computeArea(selectedShape.getPath());
            (document.getElementById("area").innerHTML = "Area =" + area.toFixed(2) * 0), 0001;
        }

        function deleteSelectedShape() {
            if (selectedShape) {
                selectedShape.setMap(null);
            }
        }

        function selectColor(color) {
            selectedColor = color;
            for (var i = 0; i < colors.length; ++i) {
                var currColor = colors[i];
                colorButtons[currColor].style.border = currColor == color ? "2px solid #789" : "2px solid #fff";
            }

            // Retrieves the current options from the drawing manager and replaces the
            // stroke or fill color as appropriate.
            var polylineOptions = drawingManager.get("polylineOptions");
            polylineOptions.strokeColor = color;
            drawingManager.set("polylineOptions", polylineOptions);

            var rectangleOptions = drawingManager.get("rectangleOptions");
            rectangleOptions.fillColor = color;
            drawingManager.set("rectangleOptions", rectangleOptions);

            var circleOptions = drawingManager.get("circleOptions");
            circleOptions.fillColor = color;
            drawingManager.set("circleOptions", circleOptions);

            var polygonOptions = drawingManager.get("polygonOptions");
            polygonOptions.fillColor = color;
            drawingManager.set("polygonOptions", polygonOptions);
        }

        function setSelectedShapeColor(color) {
            if (selectedShape) {
                if (selectedShape.type == google.maps.drawing.OverlayType.POLYLINE) {
                    selectedShape.set("strokeColor", color);
                } else {
                    selectedShape.set("fillColor", color);
                }
            }
        }

        function makeColorButton(color) {
            var button = document.createElement("span");
            button.className = "color-button";
            button.style.backgroundColor = color;
            google.maps.event.addDomListener(button, "click", function () {
                selectColor(color);
                setSelectedShapeColor(color);
            });

            return button;
        }

        function buildColorPalette() {
            var colorPalette = document.getElementById("color-palette");
            for (var i = 0; i < colors.length; ++i) {
                var currColor = colors[i];
                var colorButton = makeColorButton(currColor);
                colorPalette.appendChild(colorButton);
                colorButtons[currColor] = colorButton;
            }
            selectColor(colors[0]);
        }

        function setMapa(coords) {
            //Se crea una nueva instancia del objeto mapa
            var map = new google.maps.Map(document.getElementById("map"), {
                // zoom: 13,
                zoom: 8,
                mapTypeId: google.maps.MapTypeId.HYBRID,
                center: new google.maps.LatLng(coords.lat, coords.lng),
            });

            var goldStar = {
                path: "M 125,5 155,90 245,90 175,145 200,230 125,180 50,230 75,145 5,90 95,90 z",
                fillColor: "yellow",
                fillOpacity: 0.8,
                scale: 0.2,
                strokeColor: "gold",
                strokeWeight: 14,
            };

            var drawingManager = new google.maps.drawing.DrawingManager({
                drawingMode: google.maps.drawing.OverlayType.MARKER,
                drawingControl: true,
                drawingControlOptions: {
                    position: google.maps.ControlPosition.TOP_CENTER,
                    drawingModes: ["marker", "circle", "polygon", "polyline", "rectangle"],
                },
                markerOptions: { icon: "https://developers.google.com/maps/documentation/javascript/examples/full/images/beachflag.png" },
                polygonOptions: {
                    fillColor: "#ffffff",
                    fillOpacity: 0.9,
                    strokeWeight: 5,
                    clickable: false,
                    editable: true,
                    zIndex: 1,
                },
            });
            drawingManager.setMap(map);

            var shape = {
                coords: [1, 1, 1, 20, 18, 20, 18, 1],
                type: "poly",
            };

            //Creamos el marcador en el mapa con sus propiedades
            //para nuestro obetivo tenemos que poner el atributo draggable en true
            //position pondremos las mismas coordenas que obtuvimos en la geolocalización
            // var markerLabel = 'YO!';
            marker = new google.maps.Marker({
                map: map,
                draggable: false,
                animation: google.maps.Animation.BOUNCE,
                map: map,
                shape: shape,
                title: "Aquí estoy!",
                icon: "https://www.agronielsen.com/encampo/public/imagenes/maker-agronielsen.png",
                label: {
                    text: "Visita Técnica",
                    color: "#fff",
                    fontSize: "16px",
                    fontWeight: "bold",
                    background: "#fff",
                    width: "100px",
                },

                position: new google.maps.LatLng(coords.lat, coords.lng),
            });

            google.maps.event.addListener(drawingManager, "overlaycomplete", function (e) {
                if (e.type != google.maps.drawing.OverlayType.MARKER) {
                    // Switch back to non-drawing mode after drawing a shape.
                    drawingManager.setDrawingMode(null);

                    // Add an event listener that selects the newly-drawn shape when the user
                    // mouses down on it.
                    var newShape = e.overlay;
                    newShape.type = e.type;
                    google.maps.event.addListener(newShape, "click", function () {
                        setSelection(newShape);
                    });
                    var area = google.maps.geometry.spherical.computeArea(newShape.getPath());
                    document.getElementById("area").innerHTML = "Area =" + area.toFixed(2);
                    setSelection(newShape);
                }
            });

            // Clear the current selection when the drawing mode is changed, or when the
            // map is clicked.
            google.maps.event.addListener(drawingManager, "drawingmode_changed", clearSelection);
            google.maps.event.addListener(map, "click", clearSelection);
            google.maps.event.addDomListener(document.getElementById("delete-button"), "click", deleteSelectedShape);

            buildColorPalette();

            marker.addListener("mouseover", function (event) {
                //escribimos las coordenadas de la posicion actual del marcador dentro del input #coords
                document.getElementById("coords").value = this.getPosition().lat() + "," + this.getPosition().lng();
            });
        }
        //callback al hacer clic en el marcador lo que hace es quitar y poner la animacion BOUNCE
        function toggleBounce() {
            if (marker.getAnimation() !== null) {
                marker.setAnimation(null);
            } else {
                marker.setAnimation(google.maps.Animation.BOUNCE);
            }
        }
